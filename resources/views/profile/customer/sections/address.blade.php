            return;
        }
        
        deleteAddressId = selectedAddressId;
        
        // Find address info
        const address = addressesData.find(a => a.address_id === selectedAddressId);
        let addressInfo = 'this address';
        
        if (address) {
            const type = address.address_type || 'Unknown';
            const city = address.city || '';
            addressInfo = `${type}${city ? ' - ' + city : ''}`;
        }
        
        document.getElementById('deleteAddressInfo').textContent = addressInfo;
        document.getElementById('confirmDeleteAddressModal').classList.remove('hidden');
        document.getElementById('confirmDeleteAddressModal').classList.add('flex');
    }

    /**
     * Close delete confirmation
     */
    function closeConfirmDeleteAddress() {
        document.getElementById('confirmDeleteAddressModal').classList.add('hidden');
        document.getElementById('confirmDeleteAddressModal').classList.remove('flex');
        deleteAddressId = null;
    }

    /**
     * Confirm delete address
     */
    async function confirmDeleteAddress() {
        if (!deleteAddressId) return;

        try {
            console.log('🗑️ Deleting address:', deleteAddressId);
            
            const response = await fetch(`/api/customers/{{ $customerId }}/addresses/${deleteAddressId}`, {
                method: 'DELETE',
                headers: {
                    'Accept': 'application/json',
                    'X-Requested-With': 'XMLHttpRequest',
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]')?.getAttribute('content') || ''
                },
                credentials: 'same-origin'
            });

            const data = await response.json();
            
            if (data.success) {
                showNotification('Address deleted successfully!', 'success');
                closeConfirmDeleteAddress();
                selectedAddressId = null;
                loadAddresses();
                
                // Clear form fields
                document.getElementById('addressType').value = '';
                document.getElementById('street').value = '';
                document.getElementById('city').value = '';
                // ... clear other fields
            } else {
                showNotification('Failed to delete address: ' + (data.message || 'Unknown error'), 'error');
            }
        } catch (error) {
            console.error('❌ Error deleting address:', error);
            showNotification('An error occurred while deleting address', 'error');
        }
    }

    /**
     * Show notification
     */
    function showNotification(message, type = 'info') {
        const bgColor = type === 'success' ? 'bg-green-500' : 
                        type === 'error' ? 'bg-red-500' : 
                        type === 'warning' ? 'bg-yellow-500' : 'bg-blue-500';
        const notification = document.createElement('div');
        notification.className = `fixed top-4 right-4 ${bgColor} text-white px-6 py-3 rounded-lg shadow-lg z-50 transition-opacity duration-300`;
        notification.textContent = message;
        document.body.appendChild(notification);
        
        setTimeout(() => {
            notification.style.opacity = '0';
            setTimeout(() => notification.remove(), 300);
        }, 3000);
    }

    // Initialize
    document.addEventListener('DOMContentLoaded', function() {
        console.log('🚀 Address section initialized');
        loadAddresses();
    });

    // Close modals on outside click
    document.getElementById('addressModal')?.addEventListener('click', function(e) {
        if (e.target === this) closeAddressModal();
    });

    document.getElementById('confirmDeleteAddressModal')?.addEventListener('click', function(e) {
        if (e.target === this) closeConfirmDeleteAddress();
    });

    // Close modals on Escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            if (!document.getElementById('addressModal').classList.contains('hidden')) {
                closeAddressModal();
            }
            if (!document.getElementById('confirmDeleteAddressModal').classList.contains('hidden')) {
                closeConfirmDeleteAddress();
            }
        }
    });
